package com.lipsum.game.utils;

public class Threeple<First_T, Second_T, Third_T> {
    public First_T first;
    public Second_T second;
    public Third_T third;

    public Threeple(First_T first, Second_T second, Third_T third) {
        this.first = first;
        this.second = second;
        this.third = third;
    }
}
